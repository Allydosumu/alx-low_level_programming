/**
 * op_add - returns sum of a and b
 * @a: First number
 * @b: Second number
 *
 * Return: Sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns subtraction of a and b
 * @a: First number
 * @b: Second number
 *
 * Return: Sub of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns product of a and b
 * @a: First number
 * @b: Second number
 *
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns division of a and b
 * @a: First number
 * @b: Second number
 *
 * Return: division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns remainder of division of a and b
 * @a: First number
 * @b: Second number
 *
 * Return: Remainder of division of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
